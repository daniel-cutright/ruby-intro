# In Ruby, there are different *types* of data:

# Numbers
puts 3
puts 2
puts 2 + 3
puts 2 - 3
puts 2 * 3
puts 2 / 3
puts 2 % 3
puts 2.0 / 3

# Non-numbers
puts "Go Pats!"
puts "Riley is the best dog!"

# True, False
# puts true
# puts false

# Nothing

# Variables
x = 24
y = 7
z = 3
puts x
puts y
puts z
puts x / z

food = "tacos"
puts food

# Combine strings and variables
puts food + " are delish!"
puts "#{food} are delish!"
puts food * 3

# String manipulation
puts food.upcase
puts food.reverse